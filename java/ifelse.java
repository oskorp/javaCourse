public class ifelse {
    public static void main(String[] args) {
        int a=15;
        if(a>18){
            System.out.println("You're eligible for voting");

        }
        else{
            System.out.println("You're  not eligible for voting");

        }
        for(;;){
            System.out.println("You're  not eligible for voting");
        }
    }
}
